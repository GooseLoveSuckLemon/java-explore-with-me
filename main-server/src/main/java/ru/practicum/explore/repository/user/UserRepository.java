package ru.practicum.explore.repository.user;

import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.stereotype.Repository;
import ru.practicum.explore.model.user.User;

import java.util.List;

/**
 * Репозиторий для работы с пользователями.
 *
 * <p>Предоставляет методы для доступа к данным пользователей в базе данных.
 * Управляет пользователями системы, их регистрацией и идентификацией.
 *
 * <p>Основные операции:
 * <ul>
 *   <li>Создание и обновление пользователей</li>
 *   <li>Поиск пользователей по ID</li>
 *   <li>Поиск пользователей по списку ID</li>
 *   <li>Проверка существования пользователя по email</li>
 *   <li>Удаление пользователей</li>
 * </ul>
 *
 * <p>Особенности:
 * <ul>
 *   <li>Email пользователя должен быть уникальным</li>
 *   <li>Поддерживается поиск нескольких пользователей по списку ID</li>
 *   <li>Используется для административных операций с пользователями</li>
 * </ul>
 *
 * @author Goose
 * @version 1.0
 * @since 2026-07-01
 */
@Repository
public interface UserRepository extends JpaRepository<User, Long> {

     /**
     * Проверяет существование пользователя с указанным email.
     *
     * <p>Используется для валидации при регистрации или обновлении пользователя,
     * чтобы предотвратить дублирование email в системе.
     *
     * <p>Поиск выполняется без учёта регистра (зависит от настроек базы данных).
     *
     * @param email email для проверки
     * @return {@code true}, если пользователь с таким email уже существует,
     *         {@code false} в противном случае
     */
    boolean existsByEmail(String email);

     /**
     * Поиск пользователей по списку идентификаторов.
     *
     * <p>Возвращает список пользователей, ID которых присутствуют
     * в переданном списке.
     *
     * <p>Используется для:
     * <ul>
     *   <li>Административного поиска пользователей</li>
     *   <li>Фильтрации событий по инициаторам</li>
     *   <li>Получения информации о нескольких пользователях</li>
     * </ul>
     *
     * <p>Если список IDs пуст или содержит несуществующие ID,
     * возвращается пустой список.
     *
     * @param ids список идентификаторов пользователей
     * @return список найденных пользователей в порядке возрастания ID
     */
    List<User> findByIdIn(List<Long> ids);
}